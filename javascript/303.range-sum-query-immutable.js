/**
 * @param {number[]} nums
 */
var NumArray = function (nums) {
  const acc_sums = [0];
  for (let i = 0; i < nums.length; i++) {
    acc_sums.push(acc_sums[i] + nums[i]);
  }
  this.acc_sums = acc_sums;
};

/**
 * @param {number} left
 * @param {number} right
 * @return {number}
 */
NumArray.prototype.sumRange = function (left, right) {
  return this.acc_sums[right + 1] - this.acc_sums[left];
};

